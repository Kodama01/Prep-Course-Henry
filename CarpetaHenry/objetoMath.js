//Math pow sirve para potenciar
var potencia = Math.pow(2,3);
console.log(potencia);


//Math round redondea un decimal al entero MAS PROXIMO
var round = Math.round(5.55);
console.log(round);
//Math floor redondea al entero de MENOR valor
var floor = Math.floor(23.9);
console.log(floor);
//Math Ceil redondea al entero de MAYOR valor
var ceil = Math.ceil(23.9);
console.log(ceil);


//Math min & Math max elige al valor maximo o minimo dentro de un conjunto de numeros.
var max = Math.max(5,9,12,576,23,6,1,432)
console.log(max);
var min = Math.min(5,9,12,576,23,6,1,432)
console.log(min);


//Math random genera un valor aleatorio entre 0 y 1. Siempre es un decimal.
var random = Math.random();
console.log(random);